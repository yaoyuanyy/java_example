package com.yy.example.data_structure_and_algorithm.again_20221026.ds.linklist.leetcode.topic_2_linklist;

class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
        next = null;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}